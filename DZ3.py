# Задача 3. Создайте скрипт бота, который находит ответы на фразы по ключу в словаре.
# Бот должен, как минимум, отвечать на фразы «привет», «как тебя зовут».
# Если фраза ему неизвестна, он выводит соответствующую фразу.


def dz3():
    bot = {
        "привет": "Здравствуйте!",
        "как дела": "У меня все хорошо, спасибо.",
        "пока": "Счастливо!",
        "как тебя зовут": "Меня зовут Бот.",
        "расскажи анекдот": "Бабушка с внучкой две недели играли в школу. И только к концу второй недели, бабушка узнала, что делает за нее домашнее задание.",
        "Как тебе погода?": "в Питере, такая погода считается прекрасной",
        "спасибо": "с Вас 10$",
    }

    flag = True

    while flag:
        phrase = input("Я: ").lower()
        if phrase in bot.keys():
            print(f"Бот: {bot[phrase]}")
        else:
            print(f"Бот: Извините, я не понимаю, о чем вы говорите.")
            print(f"Бот: хотите научить меня новым фразам? (Y/N)")
            answer = input("Я: ").lower()
            if answer == "y":
                print(f"Бот: Вы написали '{phrase}'")
                print(f"Бот: Как нужно ответить на это?")
                answer = input("Я: ").lower()
                bot[phrase] = answer
            else:
                print(f"Бот: не очень то и хотелось...")

        if phrase == "пока":
            flag = False


dz3()
